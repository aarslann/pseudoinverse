    void matris(){
        String matris = null;
        for(int i = 0;i < this.m;i++){
            for(int j = 0;j < this.n;j++){
                if(i == 0 && j==0){
                    matris = Double.toString(matrix[i][j]) + "     ";
                }
                else{
                matris = matris + Double.toString(matrix[i][j]) + "     ";
                }
            }
            matris = matris + "\n\n\n";
        }
        jTextArea1.setEditable(false);
        jTextArea1.setText(matris);    
    }
    
