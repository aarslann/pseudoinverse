    void matriscarpim(){
        String matris = null;
        if(m<n){
            for (int i = 0; i < m; i++) {
            for (int j = 0; j < m; j++) { // 
                for (int k = 0; k < n; k++) {
                    carpimmatrix[i][j] += matrix[i][k] * matrix2[k][j];
                    toplamasayisi++;
                    carpmasayisi++;
                }
            }
        }    
        }        
        else if(m>n){
            for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) { // 
                for (int k = 0; k < m; k++) {
                    carpimmatrix[i][j] += matrix2[i][k] * matrix[k][j];
                    toplamasayisi++;
                    carpmasayisi++;
                }
            }
        }
        }
        if (m<n){
            for(int i = 0 ; i < m; i++){
             for(int j = 0 ; j < m; j++){
                 if(i==0 && j==0){
                     matris = Double.toString(carpimmatrix[i][j]) + "     ";
                 }
                 else{
                     matris = matris + Double.toString(carpimmatrix[i][j]) + "     ";
                 }
             }
             matris = matris + "\n\n\n";
         }
        }
        else if(m>n){
            for(int i = 0 ; i < n; i++){
             for(int j = 0 ; j < n; j++){
                 if(i==0 && j==0){
                     matris = Double.toString(carpimmatrix[i][j]) + "     ";
                 }
                 else{
                     matris = matris + Double.toString(carpimmatrix[i][j]) + "     ";
                 }
             }
             matris = matris + "\n\n\n";
         }
        }
        
jTextArea3.setEditable(false);
jTextArea3.setText(matris);
    }
    
